    private void clearTbl(){
        DefaultTableModel model = (DefaultTableModel)tblLoginRecords.getModel();
        while(model.getRowCount()>0){
            model.removeRow(0);
        }
    }
    
    
     private void userinputCharacterValidation(){
        txtFilter.getDocument().addDocumentListener(new userDocumentListener() {
            userFilterValidation invalidate = new userFilterValidation();
            @Override
            public void changedUpdate(DocumentEvent e){
                invalidate.runValidate(txtFilter);
            }
            @Override
            public void insertUpdate(DocumentEvent e) {
                invalidate.runValidate(txtFilter);
            }
            @Override
            public void removeUpdate(DocumentEvent e) {
                invalidate.runValidate(txtFilter);
            }
            
        });
    }
     
     
    private void showFilterBtn(){
        if (filter){
            btnFilter.setEnabled(true);
        }
        else
        {
            btnFilter.setEnabled(false);
        }
     } 
    
    
    private void showFilterBtn(JTextField txt){
        if ("".equals(txt.getText())){
            filter = false;
        } 
        else {
            filter = true;
        }
        showFilterBtn();
     }
    
      private void getLoginRecords(){
        try {
             File logintxt = new File(projectDir);
             BufferedReader br = new BufferedReader(new FileReader(logintxt));
            Object[] tableLines = br.lines().toArray();
            DefaultTableModel model = (DefaultTableModel)tblLoginRecords.getModel();
            for(int i = 0; i < tableLines.length; i++){
                String line = tableLines[i].toString().trim();
                String[] dataRow = line.split(":");
                model.addRow(dataRow);
             
            }
        }
        catch (FileNotFoundException ex){
            Logger.getLogger(CentreManager_LoginActivity.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    
    
     private boolean filterLoginRecords(){
        boolean Records = false;
        try {           
             File loginRecords = new File(projectDir);
             BufferedReader br = new BufferedReader(new FileReader(loginRecords));
             Object[] tableLines = br.lines().toArray();
             DefaultTableModel model = (DefaultTableModel)tblLoginRecords.getModel();
             for(int i = 0; i < tableLines.length; i++)
                {
                String line = tableLines[i].toString().trim();
                String[] dataRow = line.split(":");
                if(dataRow[0].contains(txtFilter.getText()) || dataRow[1].contains(txtFilter.getText()) || dataRow[2].contains(txtFilter.getText()) || dataRow[3].contains(txtFilter.getText())){
                     model.addRow(dataRow);
                     Records = true;                
               }
            }
             
        }
        catch (FileNotFoundException ex){
          Logger.getLogger(CentreManager_LoginActivity.class.getName()).log(Level.SEVERE, null, ex);
       }
         return Records;
    }
    
    
    
    
