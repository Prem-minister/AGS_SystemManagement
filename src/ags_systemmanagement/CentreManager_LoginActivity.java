        txtFilter.setBorder(BorderFactory.createCompoundBorder(txtFilter.getBorder(), BorderFactory.createEmptyBorder(5, 5, 5, 4)));
 
        //Disable autofucous in buttons
        btnGoBack.setFocusable(false);     
        btnClear2.setFocusable(false);
